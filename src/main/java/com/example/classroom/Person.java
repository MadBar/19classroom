package com.example.classroom;

public class Person {
	private int age = 0;
	private String firstName = "";
	private String lastName = "";
	private char gender = ' ';

	public Person(int age, String firstName, String lastName, char gender) {

		this.age = age;
		this.firstName = firstName;
		this.lastName = lastName;
		this.gender = gender;
	}
}
